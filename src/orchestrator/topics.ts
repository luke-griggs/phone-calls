// Helper function to filter topics by category
export function getTopicsByCategory(category: TopicCategory): TopicConfig[] {
  return topics.filter((t) => t.category === category);
}

// Helper function to get random topics
export function getRandomTopics(count: number): TopicConfig[] {
  const shuffled = [...topics].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}
